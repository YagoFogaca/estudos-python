# Tipos de dados
"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
Dinãmica: Ao passarmo um argumento para um função ele já reconhece seu tipo ** Olhar linha 11**
Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234) # Ele já sabe que o tipo do argumento de numero

# Aspas simples:
print('yago')

# Aspas duplas:
print("Yago")

# Aspas simples e duplas:
print(1, "Yago 'Fogaça'")
print(2, 'Yago "Fogaça"')

# Escape:
print("Yago \"Fogaça\"")

# r: É mais utilizado para expressões regulares
print(r"Yago \"Fogaça\"")